package com.upg.upc.unmsm.comon.dto;

public class ProfesorDto {

	private long profesorId;
	private String profesorNombre;
	private String profesorApePaterno;
	private String profesorApeMaterno;
	
	public long getProfesorId() {
		return profesorId;
	}
	public void setProfesorId(long profesorId) {
		this.profesorId = profesorId;
	}
	public String getProfesorNombre() {
		return profesorNombre;
	}
	public void setProfesorNombre(String profesorNombre) {
		this.profesorNombre = profesorNombre;
	}
	public String getProfesorApePaterno() {
		return profesorApePaterno;
	}
	public void setProfesorApePaterno(String profesorApePaterno) {
		this.profesorApePaterno = profesorApePaterno;
	}
	public String getProfesorApeMaterno() {
		return profesorApeMaterno;
	}
	public void setProfesorApeMaterno(String profesorApeMaterno) {
		this.profesorApeMaterno = profesorApeMaterno;
	}
	
	
}
