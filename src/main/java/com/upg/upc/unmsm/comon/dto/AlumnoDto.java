package com.upg.upc.unmsm.comon.dto;

public class AlumnoDto {

	private long alumnoId;
	private String alumnoNombre;
	private String alumnoApePaterno;
	private String alumnoApeMaterno;
	private long carreraId;
	
	public long getAlumnoId() {
		return alumnoId;
	}
	public void setAlumnoId(long alumnoId) {
		this.alumnoId = alumnoId;
	}
	public String getAlumnoNombre() {
		return alumnoNombre;
	}
	public void setAlumnoNombre(String alumnoNombre) {
		this.alumnoNombre = alumnoNombre;
	}
	public String getAlumnoApePaterno() {
		return alumnoApePaterno;
	}
	public void setAlumnoApePaterno(String alumnoApePaterno) {
		this.alumnoApePaterno = alumnoApePaterno;
	}
	public String getAlumnoApeMaterno() {
		return alumnoApeMaterno;
	}
	public void setAlumnoApeMaterno(String alumnoApeMaterno) {
		this.alumnoApeMaterno = alumnoApeMaterno;
	}
	public long getCarreraId() {
		return carreraId;
	}
	public void setCarreraId(long carreraId) {
		this.carreraId = carreraId;
	}
	
	
}
